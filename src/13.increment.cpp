#include <iostream>

using std::cout;
using std::endl;

int main(){
    // post increment
    cout << "======\n";
    // tambah
    int a = 1;
    cout << a << endl;
    cout << a++ << endl;
    cout << a << endl;
    cout << "======\n";
    cout << "======\n";
    // kurang
    int b = 1;
    cout << b << endl;
    cout << b-- << endl;
    cout << b << endl;
    cout << "======\n";
    // pre-increment
    cout << "======\n";
    //tambah
    int c = 1;
    cout << c << endl;
    cout << ++c << endl;
    cout << c << endl;
    cout << "======\n";
    cout << "======\n";
    // kurang
    int d = 1;
    cout << d << endl;
    cout << --d << endl;
    cout << d << endl;
    cout << "======\n";
    return 0;
}